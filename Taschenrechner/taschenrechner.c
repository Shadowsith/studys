#include <math.h>

int add( float a, float b) {
         return a + b;
}

int sub (float a, float b) {
         return a - b;
}

int mult (float a, float b) {
          return a * b;
}

int divs  (float a, float b) {
          return a/b;
}

int modulo (int a, int b) {
            return a%b;
}

int multiply (float a, float b) {
            return pow(a,b);
}

int rootext (float a, float b) {
            return pow(a,1/b);
}
